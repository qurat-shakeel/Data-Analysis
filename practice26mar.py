import seaborn as sns
import matplotlib.pyplot as plt
# Create age bins
age_bins = [0, 18, 30, 50, 100]
age_labels = ['0-18', '19-30', '31-50', '51+']
df['AgeGroup'] = pd.cut(df['Age'], bins=age_bins, labels=age_labels)

# bar plot of survival rates by age group
sns.barplot(x='AgeGroup', y='Survived', data=df, errorbar=None)
plt.title('Survival Rates by Age Group')
plt.xlabel('Age Group')
plt.ylabel('Survival Rate')
plt.show()

# Create a bar plot of survival rates by passenger class
