import pandas as pd
df = pd.read_csv('train.csv')
df

# counting survivors
survived_count = df['Survived'].sum()
print(survived_count)

import matplotlib.pyplot as plt
survival_counts = df.groupby('Survived').size()

survival_counts.plot(kind='bar', title='Survivors vs Non-Survivors')
plt.xlabel('Survived')
plt.ylabel('Count')
plt.xticks([0,1],['No','Yes'])
plt.show()

survival_rate = df['Survived'].mean()

print("Total number of survivors: ", survived_count)

print("Survival rate: ", survival_rate)

import seaborn as sns
import matplotlib.pyplot as plt

